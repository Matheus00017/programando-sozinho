var primeiroNumero = parseInt(prompt("Digite o primeiro número"));
var segundoNumero = parseFloat(prompt("Digite o segundo número"));

var soma = primeiroNumero + segundoNumero;
var subtracao = primeiroNumero - segundoNumero;
var multiplicacao = primeiroNumero * segundoNumero;
var divisao = primeiroNumero / segundoNumero;

console.log("o resultado da soma é " + soma);
console.log("o resultado da subtração é " + subtracao);
console.log("o resultado da multiplicação é " + multiplicacao);
console.log("o resultado da divisão é " + divisao);
